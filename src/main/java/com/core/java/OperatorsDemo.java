package com.core.java;

public class OperatorsDemo {
    public static void main(String[] args) {
        // Mathematical Operator - + (Concatenation), -, *, /, % (modulus)
        // Logical Operator - AND (&&), OR (||), NOT (!)
        // Shift Operator - TBC
        // Ternary Operator - TBC
//        String a = "5";
        int a = 5;
        int b = 7;
        System.out.println(a*b);
        System.out.println(b/a);
        System.out.println(b%a);

        // Logical Operations
        // True AND True = True
        // True OR False = True
        System.out.println(7>5 || 3>6);
    }
}
